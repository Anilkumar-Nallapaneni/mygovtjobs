/** Merge state name tables into every registered language (fallback to English). */
export function withStateNames(pack, langCode) {
  const states = STATE_NAMES[langCode] ?? STATE_NAMES.en;
  return { ...pack, states };
}

export function stateNameFor(langCode, stateId) {
  if (!stateId) return null;
  return (STATE_NAMES[langCode] ?? STATE_NAMES.en)[stateId] ?? EN[stateId] ?? stateId;
}
